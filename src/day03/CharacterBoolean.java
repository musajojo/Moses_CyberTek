package day03;

public class CharacterBoolean {

    public static void main(String[] args) {

        // Equal to ==
        // Greater than >
        // Less than <
        //Greater than/equal to >=
        // Less than/equal to <=
        // Not equal to !=

        boolean a = (2==2);
        System.out.println(a);

        boolean b = (2>2);
        System.out.println(b);

        boolean c = (2<2);
        System.out.println(c);

        boolean d = (2>=2);
        System.out.println(d);

        boolean e = (2<=2);
        System.out.println(e);

        boolean f = (2!=2);
        System.out.println(f);




    }
}
