package day07;

public class RelationalOperation_with_variable {

    public static void main(String[] args) {

        // > > = < <= == !=

        // Relational Operation is used to compare 2 oprands
        //and return true or false
        // anything acting or expression
        // // that return true or false result
        // can be called boolean expression

        int myNumber = 10;

        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5);
        System.out.println(myNumber >= 8);

        // == is used to check for equation
        // and is very different from single =

        //difference between == and = same as difference between equal and equality in daily english
        System.out.println(myNumber == 5);

        // != us used to check for inequality
        System.out.println(myNumber != 10);
        System.out.println(myNumber != 6);

        // == and != are questions, chwcking for equality or inequality






    }
}


